const nav_bar_1_menu = document.querySelectorAll('.nav-bar-1-menu');

function navBarLink_1() {
    nav_bar_1_menu.forEach((item) =>
        item.classList.remove('nav-bar-1-active'));
    this.classList.add('nav-bar-1-active');
}

nav_bar_1_menu.forEach((item) =>
    item.addEventListener('mouseover', navBarLink_1));
